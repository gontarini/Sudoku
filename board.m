% --- Executes just before board is made visible.
function board_OpeningFcn(hObject, eventdata, handles, varargin)
% This function has no output args, see OutputFcn.
% hObject    handle to figure
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% varargin   command line arguments to board (see VARARGIN)

% Choose default command line output for board
global loader;
load = loader;
global difficulty;
dif = difficulty;
set(handles.text3,'String',dif);
if load == 0
    switch dif
        case 'Easy'
            map = dlmread('40.txt');
        case 'Medium'
            map = dlmread('45.txt');
        case 'Hard'
            map = dlmread('48.txt');
        otherwise
    end
else
    h = waitbar(100,'creating map');
    board_filled = generate_board;
    close(h);
    switch dif
        case 'Easy'
            map = clear_random_cells(board_filled,40);
        case 'Medium'
            map = clear_random_cells(board_filled,45);
        case 'Hard'
            map = clear_random_cells(board_filled,48);
        otherwise
    end
end
setGlobalMap(map);

apply_listener(handles);
setMapBegining(hObject, eventdata, handles);


handles.output = hObject;

% Update handles structure
guidata(hObject, handles);

