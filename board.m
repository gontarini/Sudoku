function setMapBegining(hObject, eventdata, handles)
map = getGlobalMap;
map_vector = [];
for i =1:9
    map_vector = [map_vector map(i,:)];
end

for i =1:81
    if (~isprop(handles.uipanel1.Children(i), 'id'))
       handles.uipanel1.Children(i).addprop('id');
    end
    handles.uipanel1.Children(i).id = i;
    if map_vector(82-i) ~=0
        set(handles.uipanel1.Children(i),'String',map_vector(82-i));
        set(handles.uipanel1.Children(i),'Value',map_vector(82-i));
        set(handles.uipanel1.Children(i),'Enable','off');       
    end
end
setGlobalCond(1);
guidata(hObject, handles);



function apply_listener(handles)
setGlobalCond(0);
for ii = 1:numel(handles.uipanel1.Children)
    addlistener(handles.uipanel1.Children(ii),'String','PostSet',@editListener);
end


function editListener(handles,hObject)
if getGlobalCond ~= 0
    updateMap(get(hObject.AffectedObject,'String'),hObject.AffectedObject.id, hObject);
end



function setGlobalMap(m)
global map
map = m;


function r = getGlobalMap
global map
r = map;


function setGlobalCond(condition)
global cond;
cond = condition;

function c = getGlobalCond
global cond;
c = cond;

function updateMap(value,ind, hObject)
if isstrprop(value, 'digit')  
    if str2num(value) >9 || str2num(value) <1
        h = msgbox('Number beetwen 1 - 9','Error', 'error');
        set(hObject.AffectedObject,'String','');
        pause(2),
        delete(h)
    else
        map = getGlobalMap;
        i = 82-ind;         
        r =rem(i,9);
        switch r
            case 0
                r = 9;
                x =floor(i/9);
            otherwise
                r =rem(i,9);
                x = floor(i/9)+1;
        end
        map(x,r) = str2num(value);
        setGlobalMap(map)
    end
else
    h = msgbox('Only numbers beetwen 1-9 accepted','Error', 'error');
    set(hObject.AffectedObject,'String','');
    pause(3),
    delete(h)
end


